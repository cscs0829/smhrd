package smhrd;

public class Ex_비교 {
	public static void main(String[] args) {
	
		int num1 = 10;
		int num2 = 4;
		
		System.out.println(num1 < num2); //false
		
		// 비교연산자의 결과값은 항상 boolean 자료형이다.
		// 즉, true or false
		
		int num3 = 10;
		System.out.println(num1 == num3);
		System.out.println(num2 != num3); //같지 않다
		
		
		
		
}
}
