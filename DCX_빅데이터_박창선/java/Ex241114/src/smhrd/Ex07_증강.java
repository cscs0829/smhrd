package smhrd;

public class Ex07_증강 {

	public static void main(String[] args) {
		
		int opr = 0;
		opr += 3; // 3
		
		System.out.println(opr++); //3
		System.out.println(opr); //4
		System.out.println(++opr); //5
		System.out.println(opr); //5
		System.out.println(opr--); //5
		System.out.println(opr); //4
		System.out.println(--opr); //3
		
		
		
		

	}

}
