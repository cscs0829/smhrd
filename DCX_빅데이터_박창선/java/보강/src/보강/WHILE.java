package 보강;

import java.util.Scanner;

public class WHILE {
public static void main(String[] args) {
		
	Scanner sc = new Scanner(System.in);
	
	while(true) {
		
		System.out.print("정수 입력 : ");
		int num = sc.nextInt();
		
		
		if(num >10) {
			
		
			System.out.print("종료하겠습니다");
		break;
		}
	}
	
	
	
	
	
	
	
	
	
	
}
}
